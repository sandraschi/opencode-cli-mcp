async def _probe(port: int) -> bool:
    try:
        _, writer = await asyncio.wait_for(asyncio.open_connection("127.0.0.1", port), timeout=0.5)
        writer.close()
        await writer.wait_closed()
        return True
    except Exception:
        return False


@router.get("/fleet")
async def list_fleet():
    results = []
    for port in FLEET_PORTS:
        alive = await _probe(port)
        label = FLEET_LABELS.get(port, "unknown")
        results.append(
            {
                "port": port,
                "alive": alive,
                "label": label,
                "name": label,
                "known": port in REGISTRY_KNOWN,
            }
        )
    return {"success": True, "data": {"apps": results}}
